package JavaStudy1;

public class OperationExam2 {

	public static void main(String[] args) {
		int a = 5;
		int b = 10;
		int c = 15;
		
		System.out.println(a - b * c); //-145
		System.out.println((a - b) * c); //-75
		System.out.println(a > 5 && b > 5); //true and false
		System.out.println(a > 5 || b > 5); //true or true;
		System.out.println(a++ -5); //0
		
		System.out.println(a); //6
	}

}
